class Solution{
    public:
    // arr: input array
    // n: size of array
    //Function to find the sum of contiguous subarray with maximum sum.
    long long maxSubarraySum(int arr[], int n){
        
        // Your code here
        int cur_max = 0, mx = INT_MIN;
        
        for (int i = 0 ; i < n ; i++){
            cur_max += arr[i];
            mx =max(mx, cur_max);
            if (cur_max < 0) cur_max = 0;
        }
        return mx;
        
    }
};

